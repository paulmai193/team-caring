package com.ttth.teamcaring.config;

/**
 * The Class FcmProperties.
 *
 * @author Dai Mai
 */
public final class FcmProperties {

    /** The key. */
    private String key;

    /** The url. */
    private String url;

    /** The icon. */
    private String icon;

    /**
     * Gets the key.
     *
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * Sets the key.
     *
     * @param key
     *        the new key
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * Gets the url.
     *
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * Sets the url.
     *
     * @param url
     *        the new url
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * Gets the icon.
     *
     * @return the icon
     */
    public String getIcon() {
        return icon;
    }

    /**
     * Sets the icon.
     *
     * @param icon
     *        the icon to set
     */
    public void setIcon(String icon) {
        this.icon = icon;
    }

}
